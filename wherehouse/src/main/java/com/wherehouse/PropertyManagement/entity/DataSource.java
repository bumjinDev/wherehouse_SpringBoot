package com.wherehouse.PropertyManagement.entity;

/**
 * 매물 데이터 출처 Enum (설계 명세서 섹션 8.3).
 *
 * 매물 데이터가 어느 경로를 통해 시스템에 적재되었는지를 표현한다.
 * RDB 컬럼(DATA_SOURCE VARCHAR2(10) DEFAULT 'BATCH' NOT NULL), Redis Hash 필드,
 * F005 추천 결과 DTO, F004 목록 조회 필터에서 모두 동일한 대문자 영문 문자열로 표현된다.
 *
 * 출처 정의 (섹션 8.3):
 *   BATCH   — 기존 배치 적재 매물. BatchScheduler 가 국토교통부 API 에서 수집하여 저장한 레코드.
 *             REGISTERED_USER_ID NULL, 사용자 수정 불가(섹션 9.2.3 권한 검증 2단계에서 E4003).
 *   USER    — 사용자 등록 매물. F001 매물 등록 API 를 통해 사용자가 직접 생성한 레코드.
 *             REGISTERED_USER_ID 에 등록자 식별자 보유, F007 머지 발생 전 상태.
 *   MERGED  — 배치·사용자 매물이 동일 MD5 해시로 충돌하여 F007 머지 처리된 레코드.
 *             본 설계 범위(F001~F005)에서는 어떤 매물도 MERGED 상태가 되지 않으나,
 *             F007 구현 시점의 DDL·Enum 변경을 피하기 위해 선제 반영(섹션 11.5).
 *
 * Enum 선제 반영의 근거 (섹션 11.5):
 *   F007 구현 시점에 Enum 을 변경하면 기존 직렬화 데이터(Enum 이름 기반)의 호환성 이슈가
 *   발생할 수 있다. 본 설계에서 세 값을 미리 선언하면 F007 구현 시 Enum 코드 수정 불필요.
 *
 * 구현 주의 — 배치 매물의 데이터 출처 판정:
 *   기존 배치 적재 컴포넌트는 DATA_SOURCE 컬럼을 언급하지 않으며, Oracle 이 DDL DEFAULT
 *   값('BATCH')을 자동 주입한다(섹션 8.1.2). 즉 기존 배치 쿼리 무수정 상태에서 신규 매물이
 *   자연스럽게 BATCH 로 분류된다.
 */
public enum DataSource {

    /**
     * 배치 적재 매물. 국토교통부 API 기반, 등록자 없음.
     * 기존 BatchScheduler 경로가 생성하는 레코드의 기본 출처.
     */
    BATCH,

    /**
     * 사용자 등록 매물. F001 API 를 통해 생성된 레코드.
     * 등록자 본인만 수정·상태 변경 가능(섹션 9.2.3 권한 검증).
     */
    USER,

    /**
     * 머지 처리된 매물. F007 후속 설계에서 배치·사용자 충돌 해소 결과로 전환되는 상태.
     * 본 설계 범위에서는 어떤 매물도 이 값이 되지 않으며 Enum 선제 반영만 수행(섹션 11.5).
     */
    MERGED
}